num_uno = int(input("¿Cúal es el número que deseas convertir?: "))

if num_uno == 1:
    print("El número es 'Uno'")
elif num_uno == 2:
    print("El número es 'Dos'")
elif num_uno == 3:
    print("El número es 'Tres'")
elif num_uno == 4:
    print("El número es 'Cuatro'")
